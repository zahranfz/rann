# List
# List adalah tipe data yang dapat menyimpan banyak nilai dalam satu variabel
# List dapat menyimpan berbagai tipe data, seperti string, integer, float, dan boolean
# List dapat diubah (mutable), artinya kita dapat menambah, menghapus, atau mengubah nilai dalam list
# List dapat diakses dengan menggunakan indeks, dimulai dari 0
# List dapat menyimpan nilai duplikat
# List dapat menyimpan nilai dalam bentuk list lain (nested list)
# simbol list adalah []
# contoh list
list1 = [1, 2, 3, 4, 5]
list2 = ["a", "b", "c", "d", "e"]
list3 = [1, "a", 2.5, True]
print(list1[0])
print(list2[2])
print(list3[3])
print(list1[-1])
print(list1[-2])
